function circleArea(r){
    return Math.PI * Math.pow(r,2);
}
/**aikhane amra akta britter khetrofol ber korar jonno code likhsi 
 * amra jani britter khetrofol= (PI*r^2) 
 * amra seta function create kore Math.PI and Math.pow() 
 * object diye ber korte pari  */

console.log("Bitter khetrofol: " + circleArea(4));

function circleRange(r){
    return 2* Math.PI*r;
}
/**aikhane amra akta britter bash ber korar jonno code likhsi 
 * amra jani britter Bash= (2PI*r)  */

console.log("Bitter bash holo: " + circleRange(5));

